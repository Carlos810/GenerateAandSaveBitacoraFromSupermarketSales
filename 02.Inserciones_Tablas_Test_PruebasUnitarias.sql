-- 4. CONSULTAS DE VERIFICACIÓN
-- =============================================

-- Verificar datos insertados
SELECT 'CATEGORÍAS' AS TABLA, COUNT(*) AS REGISTROS FROM CAT_CATEGORIA
UNION ALL
SELECT 'PRODUCTOS', COUNT(*) FROM PRODUCTO
UNION ALL
SELECT 'CLIENTES', COUNT(*) FROM CLIENTE
UNION ALL
SELECT 'VENTAS', COUNT(*) FROM VENTA
UNION ALL
SELECT 'VENTA_PRODUCTOS', COUNT(*) FROM VENTA_PRODUCTOS;

-- Mostrar resumen de ventas
SELECT 
    v.ID_VENTA,
    c.NOMBRE_CLIENTE || ' ' || c.APATERNO AS CLIENTE,
    COUNT(vp.ID_PRODUCTO) AS CANTIDAD_PRODUCTOS,
    SUM(vp.CANTIDAD * vp.PRECIO_UNITARIO) AS TOTAL_VENTA,
    v.FECHA_VENTA
FROM VENTA v
JOIN CLIENTE c ON v.ID_CLIENTE = c.ID_CLIENTE
JOIN VENTA_PRODUCTOS vp ON v.ID_VENTA = vp.ID_VENTA
GROUP BY v.ID_VENTA, c.NOMBRE_CLIENTE, c.APATERNO, v.FECHA_VENTA
ORDER BY v.ID_VENTA;

COMMIT;
